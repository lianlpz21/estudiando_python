# Operaciones numéricas (con int y float)
a = 10
b = 3

suma = a + b         # 13
resta = a - b        # 7
multiplicacion = a * b  # 30
division = a / b     # 3.333...
modulo = a % b       # 1 (el resto)

# Operaciones con texto (str)
